'

# File details in long listing format.
ls -l

# Show the hidden files.
ls -a

# Detail information of files including the hidden files.
ls -la

# Print file size in human readable form.
ls -lh

# Sort the file by updated time.
ls -l --sort=time

# Sort the file by size.
ls -l --sort=size 