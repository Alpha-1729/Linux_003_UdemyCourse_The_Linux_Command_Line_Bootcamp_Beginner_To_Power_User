#!/bin/bash
# Searching And Replacing In Nano

:'
>>>> Wrapping text in nano.
        Alt + $ -> Alt + Shift +4
>>>> Search for a word in nano.
        Ctrl + W
        Alt + C -> to enable case sensitive search
        Alt + B -> to seach backward.
        Type the word and press enter
        It will search from where cursor is located.

        Ctrl + W and press Enter to find next word

>>>> Replacing string in nano.
        Ctrl + \
        Type the word to replace
        Type the replace word and press Enter.
        Type <A> to replace all words.
>>>>
'